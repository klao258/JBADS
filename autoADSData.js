    copy: (obj) => { /** 深拷贝 */
        if (obj === null || typeof obj !== "object") return obj;

        // 如果是数组
        if (Array.isArray(obj)) {
            return obj.map((item) => copy(item));
        }

        // 如果是对象
        const clone = {};
        for (const key in obj) {
            if (obj.hasOwnProperty(key)) {
                clone[key] = copy(obj[key]);
            }
        }
        return clone;
    },


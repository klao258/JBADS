    copyText: (text) => { /* 文字复制 */
        if (!text) return;
        const useClipboardAPI = navigator.clipboard && window.isSecureContext;
        if (useClipboardAPI) {
            navigator.clipboard
                .writeText(text)
                .then(() => {
                    toast("✅ 复制成功");
                })
                .catch((err) => {
                    fallback();
                });
        } else {
            fallback();
        }

        function fallback() {
            const textarea = document.createElement("textarea");
            textarea.value = text;
            textarea.style.position = "fixed"; // 避免页面滚动
            textarea.style.opacity = 0;
            document.body.appendChild(textarea);
            textarea.focus();
            textarea.select();
            try {
                const ok = document.execCommand("copy");
                toast("✅ 复制成功");
            } catch (err) {
                toast("❌ 复制异常");
            }
            document.body.removeChild(textarea);
        }
    },

    guid: (length = 8) => { /* 生成guid */
        const chars = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789";
        let result = "";
        let charsLen = chars.length;
        for (let i = 0; i < length; i++) {
            const randomIndex = Math.floor(Math.random() * charsLen);
            result += chars[randomIndex];
        }
        return result;
    },

    /* 生成随机数 */
    getRNum: (min, max, decimalPlaces = 0) => parseFloat((Math.random() * (max - min) + min).toFixed(decimalPlaces)),

    copy: (obj) => { /** 深拷贝 */
        if (obj === null || typeof obj !== "object") return obj;

        // 如果是数组
        if (Array.isArray(obj)) {
            return obj.map((item) => copy(item));
        }

        // 如果是对象
        const clone = {};
        for (const key in obj) {
            if (obj.hasOwnProperty(key)) {
                clone[key] = copy(obj[key]);
            }
        }
        return clone;
    },

    sleep: (ms) => { /** 延迟 */
        return new Promise((resolve) => {
            let timer = setTimeout(() => {
                clearTimeout(timer);
                timer = null;
                resolve(true);
            }, ms);
        })
    },

    date:  { /** 日期处理 */
        /**
         * 获取北京时间字符串
         * @param {string} format - 格式化模板，如 YYYY-MM-DD HH:mm:ss
         * @param {number} offsetDays - 相对今天偏移几天，默认 0
         */
        getBeijingString(format = 'YYYY-MM-DD HH:mm:ss', offsetDays = 0) {
          const now = new Date();
      
          // 对 UTC 时间做天数偏移
          const offsetMs = offsetDays * 24 * 60 * 60 * 1000;
          const shiftedDate = new Date(now.getTime() + offsetMs);
      
          const parts = new Intl.DateTimeFormat('en-CA', {
            timeZone: 'Asia/Shanghai',
            year: 'numeric',
            month: '2-digit',
            day: '2-digit',
            hour: '2-digit',
            minute: '2-digit',
            second: '2-digit',
            hour12: false
          }).formatToParts(shiftedDate).reduce((acc, part) => {
            if (part.type !== 'literal') acc[part.type] = part.value;
            return acc;
          }, {});
      
          return format
            .replace('YYYY', parts.year)
            .replace('MM', parts.month)
            .replace('DD', parts.day)
            .replace('HH', parts.hour)
            .replace('mm', parts.minute)
            .replace('ss', parts.second);
        },
      
        /**
         * 获取北京时间的 Date 对象
         * @param {number} offsetDays - 相对偏移几天
         */
        getBeijingDate(offsetDays = 0) {
          const str = this.getBeijingString('YYYY-MM-DDTHH:mm:ss', offsetDays);
          return new Date(str);
        },
      
        /**
         * 获取北京时间日期字符串（仅 YYYY-MM-DD）
         * @param {number} offsetDays - 相对偏移几天
         */
        getBeijingDateOnly(offsetDays = 0) {
          return this.getBeijingString('YYYY-MM-DD', offsetDays);
        },
      
        getBeijingOffset() {
          return 8;
        },
    
        /** 时间戳转日期 */
        formatCustomDate(input, format = 'MM-DD HH:mm:ss') {
            input = input?.length > 10 ? input : input * 1000; // 如果是时间戳，转换为毫秒
            const date = new Date(input);
          
            // 如果解析失败，返回空字符串
            if (isNaN(date.getTime())) return '';
          
            // 补零函数
            const pad = n => String(n).padStart(2, '0');
          
            return format
                .replace('YYYY', date.getFullYear())
                .replace('MM', pad(date.getMonth() + 1))
                .replace('DD', pad(date.getDate()))
                .replace('HH', pad(date.getHours()))
                .replace('mm', pad(date.getMinutes()))
                .replace('ss', pad(date.getSeconds()))
        },
    },

    timestampToDate: (timestamp) => { /** 时间戳转日期 */
        const date = timestamp ? new Date(timestamp) : new Date();
        const year = date.getFullYear();
        const month = String(date.getMonth() + 1).padStart(2, "0"); // Months are 0-indexed
        const day = String(date.getDate()).padStart(2, "0");

        return `${year}-${month}-${day}`;
    },

    inStr: (str, keywords) => { /** 字符串验证 keywords可传字符串或数组 */
        if (typeof str !== "string") return false;
    
        // 如果是字符串，转换成数组（以英文逗号或空格分隔）
        if (typeof keywords === "string") {
        keywords = keywords
            .split(/[, ]+/)         // 逗号或空格分隔
            .map(k => k.trim())     // 去除空格
            .filter(Boolean);       // 去除空项
        }
    
        if (!Array.isArray(keywords) || keywords.length === 0) return false;
    
        // 转义正则特殊字符
        const escaped = keywords.map(k =>
        k.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
        );
    
        const pattern = new RegExp(escaped.join("|"), "i");
    
        return pattern.test(str);
    },
